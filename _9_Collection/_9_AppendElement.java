package _9_Collection;

import java.util.HashSet;

//WAP  to append the specified element to the end of a hash set
public class _9_AppendElement 
{
	public static void main(String[] args) 
	{
		HashSet<String> hs=new HashSet<String>();
		hs.add("Aam");
		hs.add("Rani");
		hs.add("Mamayan");
		hs.add("Rami");
		hs.add("Jahul");
		
		System.out.println(hs);
		
		
	}
}
